/**********************************************************************/
/* OBJECT ATTRIBUTES FOR THIS OBJECT (C MODULE)                       */
/**********************************************************************/
#define NENTS 4

static int optab[][NENTS] = {
   {'+', integer, integer, integer},
   {'+', real,    real,    real},
   {'+', integer, real,    real},
   {'+', real,    integer, real},
   {'*', integer, integer, integer},
   {'*', real,    real,    real},
   {'*', integer, real,    real},
   {'*', real,    integer, real},
   {'$', undef,   undef,   undef}
   };

/**********************************************************************/
/*  PRIVATE METHODS for this OBJECT  (using "static" in C)            */
/**********************************************************************/

/**********************************************************************/
/*  PUBLIC METHODS for this OBJECT  (EXPORTED)                        */
/**********************************************************************/
/**********************************************************************/
/* display the op tab                                                 */
/**********************************************************************/
void p_optab()
{
    printf("------------------------------------------------\n");
    printf(" THE OPERATOR TABLE\n");
    printf("------------------------------------------------\n");
    printf("  operator       arg1         arg2     result\n");
    printf("------------------------------------------------\n");

    int i = 0; 
    while(optab[i][0] != '$'){
        printf("         %c,   %-8s,   %-8s,   %-8s\n",
            optab[i][0],
            tok2lex(optab[i][1]),
            tok2lex(optab[i][2]),
            tok2lex(optab[i][3]));
        i++;
    }
    printf("------------------------------------------------\n");
}

/**********************************************************************/
/* return the type of a binary expression op arg1 arg2                */
/**********************************************************************/
int get_otype(int op, int arg1, int arg2)
{
    int i = 0;
    while(optab[i][0] != '$') {
        if(optab[i][0] == op && optab[i][1] == arg1 && optab[i][2] == arg2)
            return optab[i][3];
        i++;
    }
    return undef;
}

